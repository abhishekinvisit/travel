<p><strong class="text-orange">What to eat</strong></p>

<p>Being a cosmopolitan city with people from all over the world, <a href="https://invisit.in/dubai-holiday-package.php">Dubai</a> is known for its top-notch international food.</p>

<ul class="blogListstyle2">
  <li><strong>Mandi</strong> – A popular meat dish</li>
  <li><strong>Oozie</strong> – Usually seen during Wedding &amp; Ramadan</li>
  <li><strong>Tabbouleh</strong> – a great option for all vegies out there</li>
  <li><strong>Kousa Mahshi</strong> – Taste great with Lebanese wine</li>
  <li><strong>Baba Ganoush</strong> – Great Starter in every party</li>
  <li><strong>Manousheh</strong> – Popularly known as Pizza Of Dubai</li>
  <li><strong>Iranian Sangak</strong> – Flat-bread of whole wheat</li>
  <li><strong>Chelo Kebab</strong> – A spice-loaded non-veg dish</li>
  <li><strong>Al Harees</strong> – Meat cooked with rice</li>
</ul>

