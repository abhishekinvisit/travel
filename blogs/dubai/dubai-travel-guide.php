<p>Though you are on a vacation trip to <a href="https://invisit.in/dubai-holiday-package.php">Dubai</a>, still you need to follow some regional rules to cope with locals.</p>

<ul class="blogListstyle">
  <li>Dress modestly</li>
  <li>Avoid affection in public</li>
  <li>Cabs are reliable and affordable</li>
  <li>Metro is the most convenient</li>
  <li>Don&rsquo;t visit Dubai during the summer</li>
  <li>Pre-book all the tickets to avoid crowd</li>
  <li>Chewing gum is prohibited in the metro</li>
  <li>Learn basic Arabic words (Thankyou, sorry, welcome, etc)</li>
</ul>

