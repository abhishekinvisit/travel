  <li>As December falls during Singapore&rsquo;s monsoon season, be prepared for showers.&nbsp;</li>
  <li>Don&rsquo;t miss out exploring bustling markets and vibrancy of Garden by the bay during Christmas</li>
  <li>Despite the rain and a little humidity, you should carry warm clothes as Singapore gets cooler at night.</li>
  <li>Be prepared for the crowd, as its one of the touristic months of the year</li>
</ul>
