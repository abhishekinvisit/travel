<p>North India is the epitome of scenic beauty and gorgeous landscapes that are yet to be explored. Take out your explorer soul to discover off-beat places with your friends or solo as well. To get an adventure, you need not spare extra time from your daily schedule, you can travel on the weekends and make it memorable forever.</p>
