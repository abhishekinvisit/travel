	<li>Visit Chandrashila Peak for panoramic views.</li>
	<li>Bird watching in Chopta. </li>
</ul>
<p><strong>What to Eat:</strong> Enjoy local Garhwali cuisine, especially the aloo ke gutke and chainsoo.</p>
<p><strong>Sightseeing:</strong> Deoria Tal, Ukhimath, and Kedarnath Wildlife Sanctuary.</p>

<h2><span>5</span> McLeodganj-Bir Billing</h2>
