  <li><strong>Chinatown</strong>: Discover the rich history and culture of Singapore's Chinese community. This is a bustling enclave that transports you to Singapore’s rich Chinese heritage.</li>
  <li><strong>Little India</strong>Experience the vibrant colors, sounds, and flavors of Indian culture. You could smell the aroma of Indian spices with Indian culture, and lively music.</li>
</ul>

