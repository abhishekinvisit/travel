<p><strong class="text-orange">What to eat in Kashmir</strong></p>

<ul class="blogListstyle">
  <li> <strong>Rogan Josh:</strong> Try out this signature dish of Kashmir, a lamb curry with aromatic spices, marinated with yogurt and lemon  </li>
  <li> <strong>Goshtaba:</strong> A traditional mutton recipe served with naan and chapati  </li>
  <li> <strong>Yakhni:</strong> Old Kashmiri delicacy, lamb meat slowly cooked with yogurt and handmade spices  </li>
  <li> <strong>Kahwa:</strong> Authentic tea recipe of Kashmir, enriched with the goodness of saffron and walnuts  </li>
  <li> <strong>Shab deg:</strong> A Kashmiri traditional dish, cooked with spices and served with both rice, chapati  </li>
</ul>


