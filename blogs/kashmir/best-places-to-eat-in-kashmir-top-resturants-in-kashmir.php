	  <div class="accordianRow">
		<h3 class="accordion">2. Which restaurant in Kashmir offers the best traditional Kashmiri Wazwan?</h3>
		<div class="expendaccordian">
		  <p>Ahdoos and Mughal Darbar are renowned for serving authentic Kashmiri Wazwan. These restaurants have been celebrated for their rich, flavorful meat dishes like Rogan Josh and Goshtaba.</p>
		</div>
	  </div>
	  
	  <div class="accordianRow">
		<h3 class="accordion">3. Are there good vegetarian restaurants in Kashmir?</h3>
		<div class="expendaccordian">
		  <p>Yes, Dana Pani in Gulmarg and Nathu Sweets in Srinagar offer excellent vegetarian options. You can enjoy classic vegetarian dishes like Rajma-Chawal, Paneer Butter Masala, and Kadhi at these places.</p>
		</div>
	  </div>
	  
	  <div class="accordianRow">
		<h3 class="accordion">4. What is the average cost of dining at a good restaurant in Kashmir?</h3>
		<div class="expendaccordian">
		  <p>Dining in Kashmir can vary in cost depending on the restaurant. On average, a meal for two at mid-range restaurants like Ahdoos or Mughal Darbar will cost between ₹1,200 to ₹2,000. Fine dining at places like The Chinar can be around ₹2,500 to ₹3,500 for two.</p>
		</div>
	  </div>
		
		
		  <div class="accordianRow">
		<h3 class="accordion">5.  Which restaurants in Kashmir offer the best views?</h3>
		<div class="expendaccordian">
