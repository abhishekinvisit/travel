		</div>
	  </div>
	  
	  <div class="accordianRow">
		<h3 class="accordion">6. Can I find international cuisine in Kashmir?</h3>
		<div class="expendaccordian">
		  <p>Yes, restaurants like Cafe De Linz in Srinagar serve Italian, Continental, and Kashmiri cuisines. These offer a blend of international and local flavors, perfect if you're looking for variety during your stay.</p>
		</div>
	  </div>
	  
	  <div class="accordianRow">
		<h3 class="accordion">7.  Is it possible to find budget-friendly restaurants in Kashmir?</h3>
		<div class="expendaccordian">
		  <p>Absolutely! Places like Alka Salka and Nathu Sweets in Srinagar offer delicious food at affordable prices, with meals costing around ₹300 to ₹1,500 for two people, depending on the dish.</p>
		</div>
	  </div>
	  
