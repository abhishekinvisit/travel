	  <div class="accordianRow">
		<h3 class="accordion">1. What are the must-try dishes in Kashmir?</h3>
		<div class="expendaccordian" style="">
		  <p>When dining in Kashmir, you should definitely try Rogan Josh, Gushtaba, Yakhni, and Tabak Maaz for a traditional Kashmiri Wazwan experience. For vegetarians, dishes like Dum Aloo and Kashmiri Haakh are flavorful options.</p>
		</div>
