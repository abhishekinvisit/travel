<p><strong class="text-orange">Detailed Itinerary</strong></p>

<h2>Day-1 Manali-Hadimba Temple-sightseeing</h2>


<ul class="blogListstyle3">
  <li>Take an overnight bus from Delhi to Manali, which takes approx. 12-15 hours</li>
  <li>As you step into Manali the fresh air will soothe all your tiredness</li>
  <li>Day starts with visiting Hadimba Devi temple followed by Vanvihar, Monastery, and Vashisht Temple</li>
  <li>Pack some extra bags to carry handmade shawls from Mall Road.</li>
  <li>Overnight stay in Manali and be ready for the ultimate fun on the next day</li>
</ul>
				  
<h2>Day 2 - Sissu-Solang Valley- Attal Tunnel</h2>


<ul class="blogListstyle3">
  <li>Start your day by treating your taste buds with popular local cuisine Siddu and Babru</li>
  <li>The second day is loaded with thrill and adventure; your sights will be blessed with stunning views of Snow showering in Sissu and the brilliant architect of Attal Tunnel</li>
  <li>Recreate &lsquo;Jab we met&rsquo; moments while crossing Solang Valley and spill out your inner geet in the snow-capped valley.</li>
  <li>Endulge in snow activities and capture beautiful pictures in local attires.</li>
</ul>
				  
<h2>Day-3 Kasol-Chalal Trek</h2>

<ul class="blogListstyle3">
  <li>The third day will be in Mini Israel of India, Kasol.</li>
  <li>The beautiful valley is located among the fresh green pines of the magnificent Parvati Valley, which serves as the meeting point for all travelers.</li>
  <li>Thrill up your tour to one level higher with paragliding and river rafting in Kasol</li>
  <li>Soak up the warmness of the bonfire and enjoy the music while camping in Riverside</li>
</ul>
				  
<h2>Day 4 Manikaran- Shopping in Kasol</h2>

<ul class="blogListstyle3">
  <li>The holistic day starts with taking a bath in the hot springs in Manikaran.</li>
  <li>Take blessings from the Lord and spend some time in Gurudwara to absorb the positiveness.</li>
  <li>Hop onto the Chalal trek to witness wooden houses, raw Himalayan beauty, and seasonal parties.</li>
  <li>You can shop in the local Kasol market, to get memorable stuff to remember your trip in Manali &amp; Kasol.</li>
  <li>It's time to return to Delhi but with a lot of memories and friends for a lifetime.</li>
</ul>
				  
